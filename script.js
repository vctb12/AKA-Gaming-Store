  getStockBadge(stock) {
    if (stock === 0) return '<div class="stock-badge out-of-stock">Out of Stock</div>';
    if (stock <= 3) return '<div class="stock-badge low-stock">Only ' + stock + ' Left</div>';
    return '<div class="stock-badge in-stock">In Stock</div>';
  }

  filterProducts(searchTerm, category) {
    let filtered = this.products;

    if (category && category !== 'all') {
      filtered = filtered.filter(product => product.category === category);
    }

    if (searchTerm) {
      filtered = filtered.filter(product =>
        product.name.toLowerCase().includes(searchTerm.toLowerCase()) ||
        product.description.toLowerCase().includes(searchTerm.toLowerCase())
      );
    }

    this.renderProducts(filtered);
  }

  addToCart(productId) {
    const product = this.products.find(p => p.id === productId);
    if (!product || product.stock === 0) return;

    const existingItem = this.cart.find(item => item.id === productId);
    
    if (existingItem) {
      existingItem.quantity += 1;
    } else {
      this.cart.push({
        id: productId,
        name: product.name,
        price: product.price,
        quantity: 1
      });
    }

    this.saveCart();
    this.updateCartCount();
    this.showNotification(`${product.name} added to cart!`);
  }

  removeFromCart(productId) {
    this.cart = this.cart.filter(item => item.id !== productId);
    this.saveCart();
    this.updateCartCount();
    this.renderCart();
  }

  updateQuantity(productId, newQuantity) {
    if (newQuantity <= 0) {
      this.removeFromCart(productId);
      return;
    }

    const item = this.cart.find(item => item.id === productId);
    if (item) {
      item.quantity = newQuantity;
      this.saveCart();
      this.updateCartCount();
      this.renderCart();
    }
  }

  saveCart() {
    localStorage.setItem('akagaming-cart', JSON.stringify(this.cart));
  }

  updateCartCount() {
    const count = this.cart.reduce((total, item) => total + item.quantity, 0);
    const cartCountElement = document.getElementById('cart-count');
    if (cartCountElement) {
      cartCountElement.textContent = count;
    }
  }

  calculateTotals() {
    const subtotal = this.cart.reduce((total, item) => total + (item.price * item.quantity), 0);
    
    // Apply AKA15 discount (15% off orders over 500 AED)
    const discountCode = document.querySelector('input[name="discount"]')?.value;
    let discount = 0;
    if (discountCode === 'AKA15' && subtotal >= 500) {
      discount = subtotal * 0.15;
    }

    // VAT 5% (UAE standard) - Free VAT if order >= 5000 AED
    const afterDiscount = subtotal - discount;
    const vat = afterDiscount >= 5000 ? 0 : afterDiscount * 0.05;
    const total = afterDiscount + vat;

    return { subtotal, discount, vat, total };
  }

  // Wishlist functionality
  toggleWishlist(productId) {
    const product = this.products.find(p => p.id === productId);
    if (!product) return;

    const existingIndex = this.wishlist.findIndex(item => item.id === productId);
    
    if (existingIndex > -1) {
      this.wishlist.splice(existingIndex, 1);
      this.showNotification(`${product.name} removed from wishlist`);
    } else {
      this.wishlist.push({
        id: productId,
        name: product.name,
        price: product.price,
        image: product.image
      });
      this.showNotification(`${product.name} saved for later!`);
    }

    this.saveWishlist();
    this.renderProducts(); // Re-render to update wishlist buttons
  }

  saveWishlist() {
    localStorage.setItem('akagaming-wishlist', JSON.stringify(this.wishlist));
  }

